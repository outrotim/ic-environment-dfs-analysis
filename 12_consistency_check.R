# E-value for protective exposure (HR < 1): use 1/HR
rr_point <- 1 / hr_point
rr_ci <- 1 / hr_ci_upper

eval_point <- rr_point + sqrt(rr_point * (rr_point - 1))
eval_ci <- rr_ci + sqrt(rr_ci * (rr_ci - 1))

cat("HR point estimate:", hr_point, "-> 1/HR =", round(rr_point, 4), "\n")
cat("HR CI upper (closest to null):", hr_ci_upper, "-> 1/HR =", round(rr_ci, 4), "\n")
cat("\nE-value (point):", round(eval_point, 2), "\n")
cat("E-value (CI limit):", round(eval_ci, 2), "\n")
cat("\nManuscript claims: E-value = 1.47 (lower CI bound 1.22)\n")
cat("CORRECT point E-value:", round(eval_point, 2) == 1.47, "\n")
